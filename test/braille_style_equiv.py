"""규정형↔도서 관행형 점형 등가 — 테스트 비교용 정규화.

`test_data/formula_pairs.json` 등의 expected는 **수학 점자 규정에서 직접 도출한 값**이라
규정형을 담고 있다(그 자체로 옳다 — 고치지 않는다). 반면 기본 실행 모드는 book이라
도서 관행형을 낸다. 두 판본이 모두 정당한 점자일 때 테스트가 어느 한쪽만 정답으로
강요하지 않도록, 비교 직전에 **양쪽을 같은 표준형으로 접는다**.
(채점기 tools/kpi/kpi_v2.py의 canon_greek과 같은 취지.)

현재 접는 항목:
  · 그리스 소문자 접두 — 규정 제30항·수학 제13항 `.x`(⠨) ≡ 도서 관행 `@x`(⠈).
    gold 수학2 실측(2026-07-21) ⠈ 263 vs ⠨ 2(val)·24 vs 0(dev), 원시 BRF에서
    θ=`@?`와 ≠=`.3`이 같은 줄에 공존해 두 접두를 구분해 쓰는 것이 확인됨.
    ⚠ ⠨은 한글 초성 ㅈ·⠈은 ㄱ이라 blanket 치환은 한글을 파괴한다 — 그리스 낱자
    점형이 바로 뒤따르는 2셀 시퀀스만 접는다.
"""

from __future__ import annotations

import re

# α β γ δ ε ζ η θ ι κ λ μ ν ξ π ρ σ τ υ φ χ ψ ω 의 낱자 점형
_GREEK_LETTER_CELLS = "⠁⠃⠛⠙⠑⠵⠱⠹⠊⠅⠇⠍⠝⠭⠏⠗⠎⠞⠥⠋⠯⠽⠺"
_GRK_RE = re.compile("[⠨⠈]([" + _GREEK_LETTER_CELLS + "])")


def canon_greek(s: str) -> str:
    """그리스 소문자 접두를 정준형 ⠨x로 접는다(규정형·관행형 모두 수용)."""
    return _GRK_RE.sub(r"⠨\1", s)
