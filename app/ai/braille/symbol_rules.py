"""특수기호 점자 매핑 (PART 4-1 전처리).

SYMBOL_TABLE: 특수기호 → 점자 BRF 직접 매핑.
substitute_symbols(): OCR 결과에 테이블 적용.
"""

from __future__ import annotations

SYMBOL_TABLE: dict[str, str] = {
    # 그리스 문자
    "α": "⠨⠁", "β": "⠨⠃", "γ": "⠨⠛", "δ": "⠨⠙",
    "ε": "⠨⠑", "θ": "⠨⠹", "λ": "⠨⠇", "μ": "⠨⠍",
    "π": "⠨⠏", "σ": "⠨⠎", "φ": "⠨⠋", "ψ": "⠨⠽",
    "ω": "⠨⠺", "η": "⠨⠓",
    # 화살표
    "→": "⠒⠕", "←": "⠐⠵", "↑": "⠐⠥", "↓": "⠐⠙", "↔": "⠒⠶",
    # 단위
    "℃": "⠘⠉", "℉": "⠘⠋",
    "㎡": "⠍⠘⠆", "㎢": "⠅⠍⠘⠆", "㎝": "⠉⠍",
    "㎞": "⠅⠍", "㎏": "⠅⠛", "㎖": "⠍⠇",
    # 수학
    "∞": "⠼⠿",
    # 원문자 (圓數字)
    "①": "⠼⠁⠲", "②": "⠼⠃⠲", "③": "⠼⠉⠲",
    "④": "⠼⠙⠲", "⑤": "⠼⠑⠲", "⑥": "⠼⠋⠲",
    "⑦": "⠼⠛⠲", "⑧": "⠼⠓⠲", "⑨": "⠼⠊⠲",
    "⑩": "⠼⠁⠚⠲",
    # 기타
    "…": "⠲⠲⠲",
    "—": "⠤⠤",
    "·": "⠐⠄",
}


def substitute_symbols(text: str) -> str:
    """SYMBOL_TABLE 기반 특수기호 점자 치환."""
    for symbol, braille in SYMBOL_TABLE.items():
        text = text.replace(symbol, braille)
    return text
